var paciente = document.querySelector('#primeiro-paciente'); 

var valopeso = paciente.querySelector('.info-peso')
var peso = parseInt(valopeso.textContent);
console.log(peso);

var valoaltura = paciente.querySelector('.info-altura');
var altura = parseInt(valoaltura.textContent);
console.log(altura);

var valogordura = paciente.querySelector('.info-gordura')
var gordura = parseInt(valogordura.textContent)
console.log(gordura)

var imc = (peso+altura+gordura)/3;
console.log(imc)
                                                                                                                                                                                                                                                                     
var substituir = paciente.querySelector('.info-imc')
substituir.textContent = imc.toFixed(2)

